"""Run the LLFF-8-scene sparse-view (N=3) sweep, baseline + DAv2.

For each scene in the canonical LLFF benchmark we run two experiments:

  1. ``{scene}_n3_baseline``  — pure 3DGS, no SSL (sanity / Δ reference).
  2. ``{scene}_n3_dav2s``     — 3DGS + multiview_photo + depth_consist
                               (Depth-Anything-V2-Small prior).

The configs mirror ``configs/llff_fern_n3_{baseline,dav2s}.yaml`` so the
results are directly comparable.  Everything is materialised on the fly
into ``configs/_llff_sweep/`` (so you can still ``scripts/eval_ckpt.py``
each run afterwards).

The sweep is designed to be *safe on 24 GB*: we keep ``cap_max=250k``
(the fern setting that ran reliably) and use the same 7000 iters /
scene. Total cost per scene ≈ 5-10 min for DAv2, ≈ 3-5 min for baseline.

Usage
-----
    python scripts/run_llff_sweep.py                # all 8 scenes, both variants
    python scripts/run_llff_sweep.py --scenes flower horns trex
    python scripts/run_llff_sweep.py --only dav2s   # skip baselines
    python scripts/run_llff_sweep.py --dry-run      # just emit configs

Run with ``--resume`` (default) to skip runs whose ``metrics.json``
already exists — safe to rerun after a crash.
"""
from __future__ import annotations

import argparse
import os
import subprocess
import sys
import time
from pathlib import Path
from typing import List, Optional

# Bootstrap
sys.path.insert(0, str(Path(__file__).resolve().parent.parent))
from scripts import _bootstrap  # noqa: E402,F401

_ROOT = _bootstrap.PROJECT_ROOT
_CFG_OUT = _ROOT / "configs" / "_llff_sweep"
_OUT_ROOT = _ROOT / "outputs"

LLFF_SCENES = ["fern", "flower", "fortress", "horns",
               "leaves", "orchids", "room", "trex"]

# Per-scene overrides.
#
# fern/flower/fortress/horns have already been trained at cap_max=250k in a
# clean, just-booted CUDA context. For the remaining scenes we saw:
#   - leaves  baseline (150k): OK
#   - leaves  dav2s    (250k): OOM @ step 807 (15 GB alloc)
#   - orchids baseline (250k): OOM @ step 912 (15.44 GB alloc)
# The default-strategy densify kernel sometimes tries to allocate
# O(N^2 / something) intermediate buffers on high-grad scenes. Cap at 180k
# to leave >5 GB headroom on a 24 GB card.
_SCENE_OVERRIDES = {
    "leaves":  {"cap_max": 150000},
    "orchids": {"cap_max": 180000},
    "room":    {"cap_max": 180000},
    "trex":    {"cap_max": 180000},
}


