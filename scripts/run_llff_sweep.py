def _emit_config(scene: str, variant: str) -> Path:
    _CFG_OUT.mkdir(parents=True, exist_ok=True)
    tmpl = _BASELINE_TMPL if variant == "baseline" else _DAV2S_TMPL
    body = tmpl.format(scene=scene)

    # Apply per-scene overrides (currently just cap_max for leaves).
    ov = _SCENE_OVERRIDES.get(scene)
    if ov and "cap_max" in ov:
        # The default body has ``cap_max: 250000`` — rewrite it.
        body = body.replace("cap_max: 250000", f"cap_max: {ov['cap_max']}")

    path = _CFG_OUT / f"llff_{scene}_n3_{variant}.yaml"
    with open(path, "w", encoding="utf-8") as f:
        f.write(body)
    return path


def _run_already_done(scene: str, variant: str) -> bool:
    d = _OUT_ROOT / f"llff_{scene}_n3_{variant}"
    m = d / "metrics.json"
    return m.is_file()


def _train(cfg_path: Path, log_path: Path) -> int:
    """Invoke ``scripts/train.py`` as a subprocess. Returns exit code.

    The child is spawned with ``CREATE_NO_WINDOW`` on Windows so it never
    pops a console (previously each child opened a cmd window that, when
    closed by the user, killed the whole process group via a CLOSE event).
    We also set ``PYTORCH_ALLOC_CONF=expandable_segments:True`` to reduce
    the chance of fragmentation-OOM on 24 GB.
    """
    py = sys.executable
    cmd = [py, str(_ROOT / "scripts" / "train.py"),
           "--config", str(cfg_path)]
    log_path.parent.mkdir(parents=True, exist_ok=True)
    print(f"  [train] {' '.join(cmd)}  > {log_path}", flush=True)

    # Windows: hide console completely.
    creationflags = 0
    if os.name == "nt":
        CREATE_NO_WINDOW = 0x08000000
        creationflags = CREATE_NO_WINDOW

    env = os.environ.copy()
    # Help PyTorch avoid big fragmented reservations.
    env.setdefault("PYTORCH_ALLOC_CONF", "expandable_segments:True")
    # Legacy name (for older PyTorch) — harmless to set both.
    env.setdefault("PYTORCH_CUDA_ALLOC_CONF", "expandable_segments:True")

    t0 = time.time()
    with open(log_path, "w", encoding="utf-8") as f:
        rc = subprocess.call(
            cmd,
            stdout=f,
            stderr=subprocess.STDOUT,
            stdin=subprocess.DEVNULL,
            cwd=str(_ROOT),
            creationflags=creationflags,
            env=env,
        )
    dt = time.time() - t0
    print(f"  [train] exit={rc}  elapsed={dt:.1f}s", flush=True)
    return rc


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--scenes", nargs="+", default=LLFF_SCENES,
                    help=f"subset of scenes; default = all 8 {LLFF_SCENES}")
    ap.add_argument("--only", choices=["baseline", "dav2s", "both"],
                    default="both")
    ap.add_argument("--dry-run", action="store_true",
                    help="emit configs but don't train")
    ap.add_argument("--no-resume", action="store_true",
                    help="overwrite runs whose metrics.json already exists")
    args = ap.parse_args()

    variants: List[str] = {"baseline": ["baseline"], "dav2s": ["dav2s"],
                           "both": ["baseline", "dav2s"]}[args.only]

    # Emit configs first (this is cheap; helpful for auditing).
    emitted: List[tuple[str, str, Path]] = []
    for scene in args.scenes:
        if scene not in LLFF_SCENES:
            print(f"[warn] unknown scene {scene!r}; skipping")
            continue
        for v in variants:
            p = _emit_config(scene, v)
            emitted.append((scene, v, p))

    print(f"[sweep] emitted {len(emitted)} configs under {_CFG_OUT}")
    for scene, v, p in emitted:
        tag = "[done]" if _run_already_done(scene, v) else "[todo]"
        print(f"  {tag}  {scene:10s}  {v:10s}  -> {p.name}")

    if args.dry_run:
        print("[sweep] --dry-run; exiting without training")
        return

    # Train.
    log_dir = _OUT_ROOT / "_llff_sweep_logs"
    log_dir.mkdir(parents=True, exist_ok=True)
    n_skip, n_ok, n_fail = 0, 0, 0
    fails: List[tuple[str, str, int]] = []
    t_sweep = time.time()
    for scene, v, cfg in emitted:
        run_name = f"llff_{scene}_n3_{v}"
        if not args.no_resume and _run_already_done(scene, v):
            print(f"[skip] {run_name} (metrics.json exists)")
            n_skip += 1
            continue
        print(f"\n[run] {run_name}")
        rc = _train(cfg, log_dir / f"{run_name}.log")
        if rc == 0 and _run_already_done(scene, v):
            n_ok += 1
        else:
            n_fail += 1
            fails.append((scene, v, rc))

    dt = time.time() - t_sweep
    print(f"\n[sweep] total elapsed {dt/60:.1f} min  "
          f"ok={n_ok} skip={n_skip} fail={n_fail}")
    if fails:
        print("[sweep] failures:")
        for s, v, rc in fails:
            print(f"  {s}/{v}: rc={rc}   "
                  f"see {log_dir / f'llff_{s}_n3_{v}.log'}")


if __name__ == "__main__":
    main()
